// 1.Write a while loop that calculates the sum of the first 10 natural numbers

public class April_loop6 {
    public static void main(String[] args) {
        int a = 1;
        int sum = 0;
        while (a<=10){
            sum += a;
            a++;
        }
        System.out.println(" Sum of given natural number is : "+sum);
    }
}
